#include<stdio.h>
int main() 
{ 
    char c = 'r';
    char d = 'R'; 
  
    // %d displays the integer value of a character 
    // %c displays the actual character 
    printf("The ASCII value of %c is %d\n", c, c); 
    printf("The ASCII value of %c is %d\n", d, d); 
    
    return 0; 
} 